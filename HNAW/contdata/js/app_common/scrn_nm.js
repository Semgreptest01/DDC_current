/***********************************************************
 *関数名：getScrnNm( id )
 *機能　：画面名称を取得する
 ***********************************************************/
function getScrnNm( id ) {
	var nm = "";
	if ( scrnNm[ id ] != undefined ) {
		nm = scrnNm[ id ];
	}
	return nm;
}
